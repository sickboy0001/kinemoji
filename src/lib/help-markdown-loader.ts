const HELP_ARTICLES: Record<string, HelpArticle> = {
  "00_index": {
    slug: "00_index",
    title: "目次",
    content: INDEX_CONTENT,
  },
  "01_quickstart": {
    slug: "01_quickstart",
    title: "クイックスタート",
    content: QUICKSTART_CONTENT,
  },
  "05_markdown": {
    slug: "05_markdown",
    title: "Markdown ガイド",
    content: MARKDOWN_CONTENT,
  },
  "90_terms": {
    slug: "90_terms",
    title: "利用規約",
    content: TERMS_CONTENT,
  },
};

/**
 * スラッグからヘルプ記事を取得
 * @param slug 記事のスラッグ
 * @returns 記事データ、見つからない場合は null
 */
export function getHelpArticle(slug: string): HelpArticle | null {
  return HELP_ARTICLES[slug] || null;
}

/**
 * 全ヘルプ記事の一覧を取得
 * @returns 記事データの配列
 */
export function getAllHelpArticles(): HelpArticle[] {
  return Object.values(HELP_ARTICLES);
}

/**
 * 有効なスラッグのリストを取得
 * @returns スラッグの配列
 */
export function getValidSlugs(): string[] {
  return Object.keys(HELP_ARTICLES);
}

/**
 * 指定されたスラッグが有効か確認
 * @param slug 確認するスラッグ
 * @returns 有効な場合は true
 */
export function isValidSlug(slug: string): boolean {
  return slug in HELP_ARTICLES;
}
